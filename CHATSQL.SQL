-- Crear la base de datos
CREATE DATABASE ECONECTA;
USE ECONECTA;

-- Tabla de usuarios para login
CREATE TABLE LOGIN (
    USUARIO VARCHAR(50) NOT NULL PRIMARY KEY,
    CONTRASEÑA VARCHAR(100) NOT NULL,
);

-- Tabla de inicio de sesión
CREATE TABLE INICIO_SESION (
    EMAIL VARCHAR(100) PRIMARY KEY,
    CONTRASEÑA VARCHAR(150) NOT NULL
);

-- Tabla de registro de usuarios
CREATE TABLE REGISTRO (
    NOMBRE VARCHAR(50) NOT NULL,
    APELLIDO_PATERNO VARCHAR(50) NOT NULL,
    APELLIDO_MATERNO VARCHAR(50) NOT NULL,
    EMAIL VARCHAR(100) PRIMARY KEY,
    FECHA_DE_NACIMIENTO DATE NOT NULL,
    EDAD INT,
    CONTRASEÑA VARCHAR(150) NOT NULL
);

-- Agregar relación entre INICIO_SESION y REGISTRO
ALTER TABLE INICIO_SESION
ADD CONSTRAINT fk_email_registro
FOREIGN KEY (EMAIL) REFERENCES REGISTRO(EMAIL)
ON DELETE CASCADE
ON UPDATE CASCADE;
